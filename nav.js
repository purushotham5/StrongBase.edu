let navLinks = document.getElementById("navLinks");
function showMenu() {
    let a;
    if (a == true) {
        navLinks.style.top = "-1000px";
    }
    else {
        navLinks.style.top = "0"
    }
} function hideMenu() {
    let a;
    if (a == true) {
        navLinks.style.top = "000px";
    }
    else {
        navLinks.style.top = "-1000px";
    }
}    
